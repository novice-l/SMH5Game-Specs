- (void)scene:(UIScene *)scene willConnectToSession:(UISceneSession *)session options:(UISceneConnectionOptions *)connectionOptions {
    // Use this method to optionally configure and attach the UIWindow `window` to the provided UIWindowScene `scene`.
    // If using a storyboard, the `window` property will automatically be initialized and attached to the scene.
    // This delegate does not imply the connecting scene or session are new (see `application:configurationForConnectingSceneSession` instead).
    UIWindowScene *windowScene = (UIWindowScene *)scene;
    self.window = [[UIWindow alloc] initWithWindowScene:windowScene];
    self.window.backgroundColor = [UIColor whiteColor];
    
    UITabBarController *tabBarController = [[UITabBarController alloc] init];
    ViewController *vc1 = [[ViewController alloc] init];
    UINavigationController *nav1 = [[UINavigationController alloc] initWithRootViewController:vc1];
    GameListRootViewController *vc2 = [[GameListRootViewController alloc] init];
    UINavigationController *nav2 = [[UINavigationController alloc] initWithRootViewController:vc2];
    UITabBarItem *item1 = [[UITabBarItem alloc] initWithTitle:@"空页面" image:nil tag:0];
    UITabBarItem *item2 = [[UITabBarItem alloc] initWithTitle:@"游戏列表" image:nil tag:1];
    nav1.tabBarItem = item1;
    nav2.tabBarItem = item2;
    tabBarController.viewControllers = @[nav1, nav2];
    self.window.rootViewController = tabBarController;
    [self.window makeKeyAndVisible];
}


